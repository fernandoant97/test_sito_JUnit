  CREATE OR REPLACE EDITIONABLE PROCEDURE "DWMANAGER"."AGGIUNGIEAN" (
    var IN VARCHAR2
) AS 
BEGIN
    DECLARE 
        numDepositi NUMBER :=0;
        art NUMBER;
    BEGIN
        SELECT count(codice) INTO numDepositi FROM deposito;
        SELECT ean.articolo INTO art FROM ean WHERE ean=var;
        FOR i IN 1..numDepositi
