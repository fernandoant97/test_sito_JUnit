  CREATE OR REPLACE EDITIONABLE FUNCTION "DWMANAGER"."PROGRESSIVOTIPOLOGIA" (
    n IN VARCHAR2
) RETURN NUMBER AS 
    cor NUMBER :=0;
    succ NUMBER :=0;
    tipo CHAR;
BEGIN
    SELECT tipologia_movimento.automatico INTO tipo FROM tipologia_movimento WHERE tipologia_movimento.codice=n;
    IF(tipo = '1')
    THEN
    SELECT tipologia_movimento.contatore INTO cor FROM tipologia_movimento WHERE tipologia_movimento.codice=n;
    succ := cor+1;
    UPDATE tipologia_movimento set contatore=succ WHERE codice=n;
    END IF;
    RETURN succ;
END ProgressivoTipologia;

