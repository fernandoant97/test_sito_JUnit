--  DDL for Function NUOVOEAN
--------------------------------------------------------

  CREATE OR REPLACE EDITIONABLE FUNCTION "DWMANAGER"."NUOVOEAN" 
(
    articolo IN NUMBER
)RETURN VARCHAR2 AS
BEGIN
    DECLARE
        pre VARCHAR2(4) := '8000';
        digit NUMBER(3,0);
        codice VARCHAR(6);
        post VARCHAR2(3);
        ean VARCHAR2(13);
    BEGIN
        IF(articolo<10)
        THEN
            codice := CONCAT('00000', TO_CHAR(articolo));
        ELSIF (articolo<100)
        THEN
            codice := CONCAT('0000', TO_CHAR(articolo));
        ELSIF (articolo<1000)
        THEN
            codice := CONCAT('000', TO_CHAR(articolo));
        ELSIF (articolo<10000)
        THEN
            codice := CONCAT('00', TO_CHAR(articolo));
        ELSIF (articolo<100000)
        THEN
            codice := CONCAT('0', TO_CHAR(articolo));
        ELSE
            codice := TO_CHAR(articolo);
        END IF;            
        SELECT dbms_random.value(100, 999) INTO digit FROM dual;
        post := TO_CHAR(digit);
        ean := CONCAT(pre, CONCAT(codice, post));
        RETURN ean;
   END;   
END NUOVOEAN;

