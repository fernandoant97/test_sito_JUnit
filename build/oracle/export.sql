  CREATE OR REPLACE EDITIONABLE PROCEDURE "DWMANAGER"."CALCOLAPREZZO" (
    var IN VARCHAR2, 
    imposta IN VARCHAR2,
    prezzo OUT NUMBER
) AS 
BEGIN   
    DECLARE
        iva NUMBER(5,2) :=0.00;
        costo NUMBER(7,2) :=0.00;
        ricarico NUMBER(5,2) :=0.00;
    BEGIN
        SELECT valore INTO iva FROM IVA WHERE codice_iva=imposta;
        SELECT costo INTO costo FROM ean WHERE ean.ean=var;
        SELECT ricarico INTO ricarico FROM ean WHERE ean.ean=var;
        prezzo := costo+((costo*iva)/100);
        prezzo := prezzo+((prezzo*ricarico)/100);
    END;
END CalcolaPrezzo;

